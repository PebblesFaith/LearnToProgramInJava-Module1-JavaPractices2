public class ScopePractice1 {
    public static void main(String[] args) {
        for (int i = 1; i <= 5; i++) {
            int squared = i * i;
            System.out.println(i + " squared  = " + squared);
        }
        System.out.println();
        for (int i = 1; i <= 5; i++) {
            int squared = i * i;
            System.out.println(i + " squared  = " + squared);
        }
    }
}
